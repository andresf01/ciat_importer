CREATE TABLE season (
  id INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  district_01 VARCHAR(255),
  ward_01 VARCHAR(255),
  village_01 VARCHAR(255),
  hhid_01 VARCHAR(255),
  Long_rain_season VARCHAR(255),
  Short_rain_season VARCHAR(255),
  Long_Dry_season VARCHAR(255),
  short_Dry_season VARCHAR(255)
);